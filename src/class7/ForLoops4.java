package class7;

public class ForLoops4 {
    public static void main(String[] args) {
        // do table of 5
        for (int i = 1; i <10; i++) {
            System.out.println("5* "+i+" = "+(5*i));
        }
        // do table of 3
        for (int i = 1; i <= 10; i++) {
            System.out.println("3* "+i+" = "+(3*i));

        }
    }
}
